# 슬라이딩 퍼즐을 풀어봅시다.
# 슬라이딩 퍼즐은 3 x 3의 판에서 각 조각들을 인접한 비어있는 칸으로 밀어서 최종적으로
# 123
# 456
# 78X
# 의 형태로 만드는 퍼즐입니다. X는 비어있는 칸을 말합니다.
# 슬라이딩 퍼즐이 입력으로 주어졌을 때, 퍼즐을 풀기 위한 최소 조각 이동 횟수를 출력하는 프로그램을 작성해주세요.

# 예제 입력1
# 12
# X3
# 예제 출력1
# 1

# 예제 입력2
# 3X
# 12
# 예제 출력2
# -1

# 입력값 설명
# 3줄에 걸쳐, 슬라이딩 퍼즐이 주어집니다.

# 출력값 설명
# 퍼즐을 풀기 위한 최소 조각 이동 횟수를 출력합니다.
# 만약 불가능하다면, 대신 -1을 출력합니다.